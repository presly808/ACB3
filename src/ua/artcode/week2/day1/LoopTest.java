package ua.artcode.week2.day1;

// Ctrl + F11
public class LoopTest {

	public static void main(String[] args) {
		
		// while
		
		int count = 0; // start 
		while(count < 10){
			System.out.println("Do work at " + count + " pos");
			count++; // way
		}
		
		// do-while
		int i = 10;
		do {
			System.out.print(i + " ");
			i -= 2;
		} while(i > 0);
		
		
		
		
	}
	

}
